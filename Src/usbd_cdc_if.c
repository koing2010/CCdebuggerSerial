uint16_t LenToken = 0;
uint16_t LenTemp = 0;
uint8_t pMsg[2200];

DATA_RX_STATUS CdcRxStatus = RX_SF_STATE;

void cdc_rx_Parse(uint8_t* Buf, uint32_t *Len)
{
uint16_t in_len = Len[0];
	if(in_len)
	{
		for(uint16_t i = 0; i < in_len; i ++)
		{
		switch(CdcRxStatus)
		{
			
		 case RX_DATA_STATE:
			  if(LenTemp < LenToken)
				{
					pMsg[LenTemp] = Buf[i];
					LenTemp ++;
					if(LenTemp == LenToken)
					{

				  CdcRxStatus = RX_FCS_STATE;

					
					}
					
				}
				else
				{
				 CdcRxStatus = RX_SF_STATE;
//		     vPortFree(pMsgStore);
//	  	   pMsg = pMsgStore = NULL;
				
				}
			  break;
		 
		 case RX_SF_STATE:
			 if(Buf[i] == 0xFE)
			 {
			 	CdcRxStatus = RX_CM_STATE;
			 }
			 break;

		case RX_CM_STATE:
		   if( (Buf[i]&0x80) != 0x80)
			 {
				 LenTemp = 1;
				 pMsg[0] = Buf[i];
				 CdcRxStatus = RX_LEN_STATE_L;
			 }
			else
				{
					CdcRxStatus = RX_SF_STATE;
				}
		   break;
		case RX_LEN_STATE_L:
			  LenToken = Buf[i];
				pMsg[LenTemp ++]  = Buf[i];
				CdcRxStatus = RX_LEN_STATE_H;
			  break;
		case RX_LEN_STATE_H:
		
			 LenToken =  ((uint16_t)Buf[i]*256)+LenToken;
			 if(LenToken <= sizeof(pMsg) )
			 {
			 	CdcRxStatus = RX_DATA_STATE;
			  pMsg[LenTemp ++] =  Buf[i];
				 
				 if(LenTemp == LenToken)
				 {
				 	CdcRxStatus = RX_FCS_STATE;
				 }

			 }
			 else
			 {
			 CdcRxStatus = RX_SF_STATE;
			 }
			 break;

			 case RX_FCS_STATE:
			 {					
				 //send msg
				 //extern uint8_t ByteCalcFCS( uint8_t *msg_ptr, uint8_t len );
				 
					if (Buf[i] == ByteCalcFCS(pMsg , LenTemp)	)
					{						
				 	extern QueueHandle_t CdcRxMsg;
					uint32_t MsgAdd = (uint32_t)pMsg;
					xQueueSendFromISR( CdcRxMsg,&MsgAdd, NULL);
						
					}
					CdcRxStatus = RX_SF_STATE;
			 }
				break;
		 default:	 
		   CdcRxStatus = RX_SF_STATE;

			 break;
		}
		
		
		}
		
	
	}


}
